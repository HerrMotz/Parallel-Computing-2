#include <stdio.h>
#include <stdint.h>

void print_bits (float f) {
    uint32_t* u = (uint32_t *) &f;
    uint32_t mask = 1 << 31;

    // 1 =                     0000 0000 0000 0000 0000 0000 0000 0001
    // 1 << 31 =               1000 0000 0000 0000 0000 0000 0000 0000
    // 0x80000000 > 1 =        0100 0000 0000 0000 0000 0000 0000 0000

    // 0x40000000 = 4 * 16^7 = 0100 0000 0000 0000 0000 0000 0000 0000
    // 4 * 16^5 =                        0100 0000 0000 0000 0000 0000

    // 3.14159628 =            0100 0000 0100 1001 0000 1111 1101 1011
    // 0x80000000 =          & 1000 0000 0000 0000 0000 0000 0000 0000
    // Ergebnis   =            0000 0000 0000 0000 0000 0000 0000 0000

    // 3.14159628 =            0100 0000 0100 1001 0000 1111 1101 1011
    //0x40000000 =           & 0100 0000 0000 0000 0000 0000 0000 0000
    // Ergebnis                0100 0000 0000 0000 0000 0000 0000 0000

    // Ausgabe                 0 10000000 10010010000111111011011
    // Ich will gerne:         01 0000000 10010010000111111011011

    do {
        if (mask == 0x40000000 || mask == 0x400000)
            putchar(' ');
        putchar(*u & mask ? '1' : '0');
    } while (mask >>= 1);
    printf("\n");
}

int main() {
    float a = 3.1415928f;
    float b = 3.1415928f;
    double c = 3;
    long double d = 3;
    long double e = 3;
    printf("a = %10.7f, a = %10.2f\n", a, b);
    printf("a = %10.8f, a = %10.2f\n", a, b);
    print_bits(a);

    return 0;
}